
export function generatePoem(theme) {
  const poems = {
    "代码与诗": [
      "每一行代码都是一句低语",
      "分号是呼吸，括号是拥抱",
      "变量在黎明前被赋值",
      "函数在午夜里递归成梦",
      "",
      "编译器不懂隐喻",
      "却把逻辑织成星图",
      "bug 是未完成的诗句",
      "debug 是反复推敲的韵脚",
      "",
      "我们用 if else 书写命运的分岔",
      "用 for 循环丈量时间的河流",
      "当程序终于运行",
      "屏幕亮起的那一刻",
      "和诗人落笔时一样——",
      "世界被轻轻改写了一行"
    ].join("\n")
  };

  if (poems[theme]) {
    return poems[theme];
  }

  // 通用模板
  const lines = [
    "以" + theme + "为名",
    "我在键盘上种下一粒种子",
    "它生长为分支，开花为提交",
    "每一次 push 都是一封寄往未来的信",
    "",
    "有人说代码是冰冷的",
    "但" + theme + "知道",
    "每个算法背后",
    "都藏着一个不肯睡去的灵魂"
  ];
  return lines.join("\n");
}
